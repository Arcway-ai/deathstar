    def _cleanup_agent(self, agent: RunningAgent) -> None:
        """Remove an agent and release its branch lock."""
        self._agents.pop(agent.conversation_id, None)
        if agent.branch:
            lock_key = (agent.repo, agent.branch)
            if self._branch_locks.get(lock_key) == agent.conversation_id:
                self._branch_locks.pop(lock_key, None)

    async def _reaper_loop(self) -> None:
        """Periodically clean up stale and completed agents."""
        while True:
            await asyncio.sleep(60)
            try:
                now = time.time()
                to_remove: list[str] = []
                for conv_id, agent in list(self._agents.items()):
                    if agent.status in (AgentStatus.COMPLETED, AgentStatus.FAILED):
                        if agent.completed_at and now - agent.completed_at > _COMPLETED_RETAIN_SECONDS:
                            to_remove.append(conv_id)
                    elif now - agent.last_active > _AGENT_TTL_SECONDS:
                        # Stale running agent — evict
                        logger.info("evicting stale agent: conv=%s branch=%s", conv_id, agent.branch)
                        try:
                            await agent.client.disconnect()
                        except Exception:
                            pass
                        to_remove.append(conv_id)

                for conv_id in to_remove:
                    agent = self._agents.get(conv_id)
                    if agent:
                        self._cleanup_agent(agent)
                        logger.info("reaped agent: conv=%s status=%s", conv_id, agent.status)

            except asyncio.CancelledError:
                raise
            except Exception:
                logger.warning("agent reaper error", exc_info=True)
